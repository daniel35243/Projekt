package Database;

public class GameByID {
    public int ID;
    public int Coins;
    public int Level;
    public int XP;
    public int Tag;
    public int Uhr;

    public GameByID(int ID, int Coins, int Level, int XP, int Tag, int Uhr) {
        this.ID = ID;
        this.Coins = Coins;
        this.Level = Level;
        this.XP = XP;
        this.Tag = Tag;
        this.Uhr = Uhr;
    }
}
